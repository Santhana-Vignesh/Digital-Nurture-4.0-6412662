public class AssertionMain {
    public static void main(String[] args) {
        System.out.println("2 + 3 = " + (2 + 3));
        System.out.println("5 > 3 is " + (5 > 3));
        System.out.println("5 < 3 is " + (5 < 3));
        System.out.println("null is null: " + (null == null));
        System.out.println("new Object() is not null: " + (new Object() != null));
    }
}
